class Paciente {
    constructor(
        id,
        codigoAcesso,
        dataNascimento,
        altura,
        peso,
        nome,
        nomeAcesso,
        imagem,
        email,
        telefone
    ) {
        this.id = id;
        this.codigoAcesso = codigoAcesso;
        this.altura = altura;
        this.dataNascimento = dataNascimento;
        this.peso = peso;
        this.nome = nome;
        this.nomeAcesso = nomeAcesso;
        this.imagem = imagem;
        this.email = email;
        this.telefone = telefone;
    }
};
